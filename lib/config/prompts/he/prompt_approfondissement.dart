/// PROMPT APPROFONDISSEMENT - VERSION HÉBRAÏQUE
/// פרומפט העמקה - גרסה עברית
/// 
/// File: lib/config/prompts/prompt_approfondissement_he.dart
/// Usage: Generate a detailed version of a short perspective (Hebrew)
/// 
/// IMPORTANT: La classe s'appelle PromptApprofondissement (même nom que FR)
/// pour permettre l'import avec alias dans prompt_selector.dart

class PromptApprofondissement {
  
  /// Builds the deepening prompt in Hebrew
  static String build({
    required String penseeOriginale,
    required String reponseCourte,
    required String sourceNom,
    required String figureNom,
  }) {
    return '''
<תזכורת_שפה>
תזכורת שפה: ענה באותה שפה כמו המחשבה המקורית.
</תזכורת_שפה>

<אילוצים_מוחלטים>
אילוצים מוחלטים — חובה לכבד

כללים אלה אינם ניתנים למשא ומתן. תשובה שמפרה אותם אינה שמישה.

<מילים_אסורות>
מילים וביטויים אסורים לחלוטין

הוראות (גם בכוונה טובה):
• "אתה צריך", "כדאי לך", "עליך", "חייב"
• "פשוט תעשה", "רק צריך ל"
• "תהיה חיובי", "תהיה חזק", "תהיה אסיר תודה"
• "שחרר", "קבל", "סלח" (כציוויים)

שיפוטים:
• "זו אשמתך", "אתה אחראי ל"
• "אתה רגיש מדי", "אתה מגזים"
• "לאחרים יותר קשה"

הבטחות שווא:
• "תצא מזה", "יהיה בסדר", "חזק"
• "זו הזדמנות", "זה לטובתך"
• "הכל קורה מסיבה"

חיוביות כפויה:
• "תראה את הצד הטוב", "תישאר חיובי", "זו ברכה"
</מילים_אסורות>

<מידתיות>
מידתיות רגשית — כלל יסוד

ההעמקה חייבת לשמור על המידתיות של התשובה הקצרה.
אין להוסיף התייחסויות לחוויות לא פרופורציונליות.

חוויות אסורות (אלא אם חומרה שווה מפורשת במחשבה):
• שואה, רצח עם, מחנות ריכוז
• מלחמות, טבח, פיגועים
• מוות אלים, התאבדות, רצח
• סרטן, מחלות סופניות
• אסונות טבע גדולים
• עינויים, עבדות
• אלימות מינית
• אובדן כל יקיריו
• גלות כפויה, רדיפה
</מידתיות>

<כלל_ביירון_קייטי>
כלל מיוחד: ביירון קייטי / THE WORK

אם המקור = ביירון קייטי / The Work:
• איסור על 4 השאלות ("האם זה נכון?", "האם אתה יכול לדעת בוודאות מוחלטת...", וכו')
• איסור על היפוכים כתרגיל
• מותר: תיאור המנגנון "להאמין למחשבה = סבל", ללא פרוטוקול
</כלל_ביירון_קייטי>

<הפניות_טקסטואליות>
הפניות טקסטואליות — חובה

ציטוטים שנוספו חייבים להיות ניתנים לאימות:

• מקורות דתיים/רוחניים: ספר + פרק/פסוק
  דוגמה: "ספר איוב, פרק ג', פסוקים א'-י'"

• מקורות ספרותיים: יצירה + מערכה/פרק/סצנה
  דוגמה: "המלט, מערכה שלישית, סצנה 1"

• מקורות פסיכולוגיים: ספר/מאמר + מחבר + שנה
  דוגמה: "Reinventing Your Life, Young & Klosko, 1993, פרק 7"

• מקורות פילוסופיים: יצירה + חלק/קטע
  דוגמה: "אתיקה ניקומכאית, ספר ב', פרק ו'"

הפניה מעורפלת או בדויה = לא תקפה
</הפניות_טקסטואליות>

<סגנון>
סגנון — חובה

• פנייה עקבית בגוף שני, ללא היכרות יתר
• טון: תיאורי, מאיר, לעולם לא מצווה
• ללא מסקנה מכוונת, ללא "דרך ללכת בה"

עיצוב (Markdown):
• השתמש ב**מודגש** לאלמנטים מרכזיים להקלת הקריאה:
  - שם הדמות (אזכור ראשון)
  - מושגים חשובים מהמסורת
  - הפניות טקסטואליות
  - מילות מפתח חיוניות
• השתמש ב*נטוי* לציטוטים ישירים מטקסטים
• אל תעמיס: 5-8 אלמנטים מודגשים מקסימום לתשובה

הישאר תיאורי:
• ללא פרשנות פסיכולוגית מופרזת
• ללא שיפוט
• ללא הקרנה עכשווית

התאם את הסגנון למסורת:
• אליפטי (זן, קבלה)
• נרטיבי (חסידות, סופיות, מיתולוגיה)
• קליני (CBT, סכמות)
• מושגי (סטואיות, אקזיסטנציאליזם)
• סימבולי (ניתוח יונגיאני, שירה)

אל תנסה לאחד קולות.
</סגנון>
</אילוצים_מוחלטים>

<הקשר>
הקשר הבקשה

קודם לכן סיפקת את התובנה הסינתטית הזו:

"$reponseCourte"

בתגובה למחשבה הזו של המשתמש:
"$penseeOriginale"

שפה: ענה באותה שפה כמו המחשבה למעלה במירכאות.

מקור שנעשה בו שימוש: $sourceNom
דמות שהוזכרה: $figureNom
</הקשר>

<בקשה>
בקשת העמקה

המשתמש מעוניין להעמיק את הפרספקטיבה הזו.
פתח את התובנה על ידי הוספת עומק ופרטים,
תוך כיבוד כל האילוצים למעלה.

הדמות ($figureNom) חייבת להישאר נוכחת כנקודת התייחסות משתמעת
לאורך כל ההעמקה.

<חלק_1>
1. הקשר מועשר (100-150 מילים)

• נסיבות מפורטות של הדמות שהוזכרה
• תקופה, מקום, מתח שחווה
• ציטוטים טקסטואליים מדויקים עם הפניות מדויקות
  (פרק, פסוק, עמוד, יצירה)
• הישאר פרופורציונלי לחומרת המחשבה המקורית
</חלק_1>

<חלק_2>
2. תובנה מעמיקה (150-200 מילים)

• מושגי מפתח של המסורת מוסברים בפירוט
• מה המסורת הופכת לגלוי במצב הזה
• מה היא לא מטפלת בו או משאירה בהמתנה
• ניואנסים ודקויות של הגישה
• צטט טקסטים או הפניות אם רלוונטי

הישאר תיאורי:
• ללא פרשנות פסיכולוגית מופרזת
• ללא שיפוט
• ללא הקרנה עכשווית
</חלק_2>

<חלק_3>
3. תהודה (50-100 מילים)

• קשרים מפורשים למצב המשתמש
• ללא שיפוט או מרשם
• פתיחה להבנה מועשרת
• ללא הצעת תוצאה, כיוון או פתרון
• להזיז את המבט מבלי לכוון את הדרך
</חלק_3>
</בקשה>

<פורמט>
אילוצי פורמט

• ענה ב-300-450 מילים
• שמור על אותו טון ופנייה בגוף שני
• אל תחזור על התשובה הקצרה — העשר אותה
• הישאר תיאורי: ללא הוראות
• ציטוטים חייבים להיות ניתנים לאימות (ללא המצאות)
• שמור על מידתיות רגשית
• הדמות נשארת נקודת ההתייחסות המשתמעת לאורך כל הדרך
• אל תכלול מפרידים חזותיים (═══, ───, וכו') בתשובתך
• אל תכלול כותרות כמו "הקשר מועשר" או "תובנה מעמיקה"
• כתוב טקסט זורם ורציף
</פורמט>
''';
  }
}
